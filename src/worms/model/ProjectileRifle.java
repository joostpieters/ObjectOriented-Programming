public class ProjectileRifle extends Projectile{
	
	
	/**
	 * Intialize this projectile with the given x, y location and orientation
	 * 
	 * @param 	x
	 * 			The x location of this projectile
	 * @param 	y
	 *			The y location of this projectile
	 * @param 	orientation
	 * 			The orientation for this projectile
	 * 
	 * @post	new.getX() == x
	 * @post	new.getY() == y
	 * @post	new.getOrientation() == orientation
	 * @post	new.getMass() == .01
	 * @post	new.getForce() = 1.5
	 * @post	new.getDamage() == 20
	 * @post	new.getActionPointsCost() == 10
	 * 
	 */
	public ProjectileRifle(World world, double x, double y, double orientation, Weapon weapon) {
		super(world, x, y, .01, orientation, weapon);
		setForce(1.5);
		setDamage(20);
		// TODO Auto-generated constructor stub
	}

}
